    private void btn_araActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_araActionPerformed
       dt.getDataVector().clear();
       tblListe.removeAll();
       
        String aranan = "";
        String gelen = txt_urunAra.getText();
        String[] dizi = gelen.split(" ");
        
        for(String item : dizi) {
            aranan += item + "*";
        }
        try {
            MySQLConnection db = new MySQLConnection();
            ResultSet rs = db.st.executeQuery("call fullTextAra('"+aranan+"')");
           // ResultSet rs = db.st.executeQuery("select * from ariza inner JOIN cihaz on ariza.cihaz_id = cihaz.id where Match (ariza.musteri_adi) AGAINST('"+aranan+"*' IN Boolean MODE)");
            while(rs.next()) {
                dt.addRow(new String[]{rs.getString("id"), rs.getString("musteri_adi"), rs.getString("musteri_tel"), rs.getString("adi"), rs.getString("markasi"), rs.getString("modeli"), rs.getString("arizasi")});
            }
           
            tblListe.setModel(dt);
        } catch (Exception e) {
        }
        
    }//GEN-LAST:event_btn_araActionPerformed

    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed
        int secilenID = tblListe.getSelectedRow();
        String gtr = list.get(secilenID);
        System.out.println(gtr);
       
        
        MySQLConnection db = new MySQLConnection();
        try{
            int sonuc = db.dataUpdate("update ariza set durum = '"+0+"' where id = '"+gtr+"' ");
            if(sonuc > 0) {
                JOptionPane.showMessageDialog(this, "Arşive Eklendi");
                tblListe.removeAll();
                dt.getDataVector().clear();
                tabloDoldur();
            }
        } catch(Exception e) {
            
        }
       
    }//GEN-LAST:event_jButton4ActionPerformed

