     public void tabloDoldur() {
         MySQLConnection db = new MySQLConnection();
        try {
            ResultSet rs = db.data("ariza inner join cihaz on cihaz.id = ariza.cihaz_id");
            while (rs.next()) {
                if(rs.getString("durum").equals("1")) {
                 list.add(rs.getString("id"));
                 dt.addRow(new String[]{rs.getString("id"), rs.getString("musteri_adi"), rs.getString("musteri_tel"), rs.getString("adi"), rs.getString("markasi"), rs.getString("modeli"), rs.getString("arizasi")});
            }
            }
        } catch (Exception e) {
        }
        tblListe.setModel(dt);
     }

